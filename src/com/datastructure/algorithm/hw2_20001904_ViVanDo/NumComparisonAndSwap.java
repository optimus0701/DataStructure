package com.datastructure.algorithm.hw2_20001904_ViVanDo;

public class NumComparisonAndSwap {
    private int numOfComparisons;
    private int numOfSwap;

    public NumComparisonAndSwap(int compare, int swap) {
        this.numOfComparisons = compare;
        this.numOfSwap = swap;
    }

    public int getNumOfComparisons() {
        return numOfComparisons;
    }

    public void setNumOfComparisons(int numOfComparisons) {
        this.numOfComparisons = numOfComparisons;
    }

    public int getNumOfSwap() {
        return numOfSwap;
    }

    public void setNumOfSwap(int numOfSwap) {
        this.numOfSwap = numOfSwap;
    }
}
